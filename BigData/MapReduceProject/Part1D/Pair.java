
public class Pair {
	private Integer sum;
	private Integer count;
	Pair(Integer sum, Integer count) {
		this.sum = sum;
		this.count = count;
	}
	public Integer getSum() {
		return sum;
	}
	public void setSum(Integer sum) {
		this.sum = sum;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}

}
