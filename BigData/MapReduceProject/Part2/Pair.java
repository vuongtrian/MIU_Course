public class Pair {
	private String key1;
	private String key2;
	Pair(String key1, String key2) {
		this.key1 = key1;
		this.key2 = key2;
	}
	public String getKey1() {
		return key1;
	}
	public void setKey1(String key) {
		this.key1 = key;
	}
	public String getKey2() {
		return key2;
	}
	public void setKey2(String key) {
		this.key2 = key;
	}
}
